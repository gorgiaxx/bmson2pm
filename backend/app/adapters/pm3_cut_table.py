"""Built-in static PM3 cut table (indexed).

The A36 FPGA ROM stores 938 per-chart "cut" entries (48 bytes each at offset
0x7678: a 32-byte filename followed by 16 bytes of cut data). A ``.enccut``
file is an ``.enc`` chart with its first 16 bytes removed; the cut data supplies
those bytes so the ciphertext stream can be reconstructed for decryption.

The 16-byte cut is a 4-byte header plus a 12-byte ciphertext prepend. The
header is a pure slot selector: ``slot_for_header`` reads only 4 bits of it to
pick one of 10 FPGA key slots (slot0..slot9), the remaining 28 bits are
ignored. Across all 938 charts there are only 16 distinct (slot, prepend) pairs,
so the table is stored as 16 unique cut values (indexed 0..15) plus a
filename -> index map. Headers are canonicalized via ``header_for_slot(slot)``,
which decrypts identically to the ROM's per-chart headers (verified against
real .enccut files).
"""

from .pm3_crypto import header_for_slot


def _canonical_cut(slot: int, prepend_hex: str) -> bytes:
    return header_for_slot(slot).to_bytes(4, "little") + bytes.fromhex(prepend_hex)


